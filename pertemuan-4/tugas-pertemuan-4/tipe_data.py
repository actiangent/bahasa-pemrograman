
# String
a = "Hello World"
print(a)
print(type(a))

# integer
b = 13
print(b)
print(type(b))

# float
c = 11.5
print(c)
print(type(c))

# tuple
d = 21, 5
print(d)
print(type(d))

tuple_f = 1, 2, 3
print(tuple_f[0])

# complex number
e = 1j
print(e)
print(type(e))

z = complex('5-9j')
print(z)

# list
f = ["a", "b", "c"]
print(f)
print(type(f))

list_f = [1, 2, 3]
print(list_f[0])

# set
set_f = {1, 2, 3}
print(set_f)

g = {"a", "b", "c"}
print(g)
print(type(g))

# frozenset
h = frozenset({1, 2, 3})
print(type(h))

# boolean
i = True
j = False
print(i, j)
print(type(i))
print(type(j))
